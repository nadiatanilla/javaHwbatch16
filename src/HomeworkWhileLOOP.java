public class HomeworkWhileLOOP {
    public static void main(String[] args) {
        int x=1;
        while(x<=50){
            if(x%3!=0){
                System.out.print(x+" ");
            }
            x++;

        }
        System.out.println();


        for(int z=1;z<=50;z++){
            if(z%3 !=0){
                System.out.print(z+" ");
            }

        }
    }
}
