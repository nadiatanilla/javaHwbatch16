package Class4;

public class E12NestedIf {
    public static void main(String[] args) {
        System.out.println("3");
        if (10 > 5) {
            System.out.println("1");
            if (20 > 3) {
                System.out.println("2");
            }
        }
        System.out.println("4");
    }
}