package class7;

public class E6SwitchStatement {
    public static void main(String[] args) {
        String name="Nabil";

        switch (name){
            case "Vlad":
                System.out.println("Algonquin");
                break;
            case "Jacob":
                System.out.println("Chicago");
                break;
            case "Ahmed":
                System.out.println("Morocco");
            default:
                System.out.println("West America");

        }
    }
}
