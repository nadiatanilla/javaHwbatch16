package ReviewClass;

public class NestedLoops {
    public static void main(String[] args) {
        for(int a=0;a<=3;a++){
            for(int b=0;b<=3;b++){
                for(int c =0;c<=3;c++){
                    System.out.println(a+" "+b+" "+c);
                }
            }
        }

    }
}
